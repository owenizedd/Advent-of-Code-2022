function bfs(sy, sx) {
  let q = []
  const E = { y: 20, x: 119 }
  q.push([sy, sx, 0]);
  const dist = (x, y, tx, ty) => {
    return inp[y][x].charCodeAt(0) - inp[ty][tx].charCodeAt(0)
  }
  const vis = {};


  while (q.length) {
    const [y, x, step] = q[0];
    // p(y, x);
    q.shift();
    const key = `${y}, ${x}`;
    if (vis[key] !== undefined) continue;

    vis[key] = 1;
    let canRight = true;
    let canLeft = true;
    let canTop = true;
    let canBottom = true;
    if (x + 1 >= xLen || dist(x, y, x + 1, y) < -1)
      canRight = false
    if (x - 1 < 0 || dist(x, y, x - 1, y) < -1)
      canLeft = false
    if (y - 1 < 0 || dist(x, y, x, y - 1) < -1)
      canTop = false
    if (y + 1 >= yLen || dist(x, y, x, y + 1) < -1)
      canBottom = false

    if (x == E.x && y == E.y) {
      return step;
    } else {
      if (canLeft) {
        q.push([y, x - 1, step + 1])
      }
      if (canRight) {

        q.push([y, x + 1, step + 1])
      }
      if (canBottom) {

        q.push([y + 1, x, step + 1])
      }
      if (canTop) {

        q.push([y - 1, x, step + 1])
      }
      // p('cur', y, x, vis);
    }
  }
  return 1000000000;
}
